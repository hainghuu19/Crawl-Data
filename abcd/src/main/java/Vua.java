public class Vua {
    private String nienHieu;
    private String chuHan;
    private String tayLich;
    private String tenVua;


    public Vua(String nienHieu, String chuHan, String tayLich, String tenVua){
        this.nienHieu = nienHieu;
        this.chuHan = chuHan;
        this.tayLich = tayLich;
        this.tenVua = tenVua;
    }

    public String getNienHieu() {
        return nienHieu;
    }



    public String getChuHan() {
        return chuHan;
    }



    public String getTayLich() {
        return tayLich;
    }



    public String getTenVua() {
        return tenVua;
    }


}
